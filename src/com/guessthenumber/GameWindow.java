    public void check(){
        if(chance==1){
            if(Integer.parseInt(jTextField1.getText())==num){
                Result res=new Result(true,num,chance,d,defChance);
                res.show();
                dispose();
            }
            else{
            Result res=new Result(won,num,chance,d,defChance);
            res.show();
            dispose();
            }
        }
        else{
            if(Integer.parseInt(jTextField1.getText())==num){
                Result res=new Result(true,num,chance,d,defChance);
                res.show();
                dispose();
            }
            if(Integer.parseInt(jTextField1.getText())<num){
                jLabel1.setText("Too Low!");
                jLabel1.setForeground(new java.awt.Color(240, 240, 240));
            }
            if(Integer.parseInt(jTextField1.getText())>num){
                jLabel1.setText("Too High!");
                jLabel1.setForeground(new java.awt.Color(240, 240, 240));
            }
            if((Integer.parseInt(jTextField1.getText())+1==num)||(Integer.parseInt(jTextField1.getText())-1==num)){
                jLabel1.setText("Almost There!");
                jLabel1.setForeground(new java.awt.Color(234, 198, 198));
            }
            if((Integer.parseInt(jTextField1.getText())+2==num)||(Integer.parseInt(jTextField1.getText())-2==num)){
                jLabel1.setText("Pretty Close!");
                jLabel1.setForeground(new java.awt.Color(240, 240, 240));
            }
            //add to table
            if(jTextField1.getText().equals("")){
                jLabel1.setText("Enter a valid number");
            }
            else{
                String data[]={jTextField1.getText(),jLabel1.getText()};
                DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
                model.addRow(data);
            }
            jLabel2.setText((chance-1)+" chances left");
            chance--;
        }
        
        jTextField1.setText("");
    }
